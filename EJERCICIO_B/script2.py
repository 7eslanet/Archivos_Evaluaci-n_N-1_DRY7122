# Script para solicitar y mostrar información del usuario

# Solicita información al usuario
nombre = input("Ingresa tu nombre: ")
apellido = input("Ingresa tu apellido: ")
codigo_seccion = input("Ingresa tu código de sección: ")
sede = input("Ingresa tu sede: ")

# Muestra la información recolectada
print("\nInformación del usuario:")
print(f"Nombre: {nombre}")
print(f"Apellido: {apellido}")
print(f"Código de Sección: {codigo_seccion}")
print(f"Sede: {sede}")
